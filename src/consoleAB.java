private String buscar(String texto, String p1, String p2) {

        int ini = 0, fin = 0;
        String cad;
        char a;

        if (texto.indexOf(p1) != -1) {

            ini = texto.indexOf(p1);

            if (texto.indexOf(p2) != -1) {
                fin = texto.indexOf(p2);
            }

        }

        cad = texto.substring(ini, fin);
        return cad;
    }

public static boolean esNumero(String str) {
        try {
            int d = Integer.parseInt(str);
            if(d>0){
                return true;
            }else{
                return false;
            }
        } catch (NumberFormatException nfe) {
            return false;
        }
    }

public void llenarListas (String text) {
    
            String[] auxi = {};
            String au;
    if (text.indexOf("(ms):") != -1) {

                au = text.replace("Porcentaje de velocidad de transmision (ms):", "");

                au = au.replace(" ", "");
                au = au.replace("\n", ",");
                au = au.replace("%", ",");

                auxi = au.split(",");

                for (int i = 1; i < auxi.length; i++) {

                    if (i % 2 == 0) {
                        if (esNumero(auxi[i].toString())) {
                            listax.add(Float.parseFloat(auxi[i].toString()));
                        }

                    } else {
                        if (esNumero(auxi[i].toString())) {
                            listaY.add(Float.parseFloat(auxi[i].toString()));
                        }

                    }

                }

            }
}

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String cmd [] = {"ab","-n", this.n_peticiones.getText(), this.url_text.getText()};
        try {
            Process p = Runtime.getRuntime().exec(cmd);
            p.waitFor();
            BufferedReader reader = new BufferedReader(new InputStreamReader(p.getInputStream()));
            String cad="";
            String line="";
            
            while((line = reader.readLine()) != null) {
                cad=cad+line+"\n";
            }
            String numeroBites = buscar(cad, "Total transferred:", "HTML");
            String velTran = buscar(cad, "Requests per second:", "(mean)");
            velTran = velTran.replaceAll("Requests per second:", "Velocidad de transferencia");
            numeroBites = numeroBites.replaceAll("Total transferred:", "Numero de Bites:");
            String port = buscar(cad, "Percentage of the requests served within a certain time (ms)", "(longest request)");
            port = port.replace("Percentage of the requests served within a certain time (ms)", "Porcentaje de velocidad de transmision (ms): ");
        
            llenarListas(port);
            listax.forEach((n) -> System.out.println(n));
            System.out.println("-------- y --------");
            listaY.forEach((n) -> System.out.println(n));
            cad = cad.trim();
            this.log.setText(cad);
            this.txt_veltran.setText(velTran);
            System.out.println("----------");
            this.txt_vnbytes.setText(numeroBites);
            System.out.println(velTran);
            RegresionDatos.main(cmd);
            TextToPDF.TextToPDF(cad);
        } catch (IOException ex) {
            Logger.getLogger(consoleAB.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InterruptedException ex) {
            Logger.getLogger(consoleAB.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1MouseClicked

